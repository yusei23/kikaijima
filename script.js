const phoneGame = document.querySelector('.ellipse');

function handleScroll() {
    const elementOffset = phoneGame.offsetTop;
    const scrollPosition = window.pageYOffset || document.documentElement.scrollTop;
    const viewportBottom = scrollPosition + window.innerHeight;
    const threshold = viewportBottom * 0.9;

    if (elementOffset < threshold) {
        phoneGame.classList.add('show');
    }
}

window.addEventListener('scroll', handleScroll);


